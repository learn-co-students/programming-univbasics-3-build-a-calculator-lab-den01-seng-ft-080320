def addition(num1, num2)
  total = num1 + num2
  p total
end

def subtraction(num1, num2)
  total = num1 - num2
  p total
end

def division(num1, num2)
  totaldiv = num1 / num2
  p totaldiv
end

def multiplication(num1, num2)
  totalmulti = num1 * num2
  p totalmulti
end

def modulo(num1, num2)
  totalmod = num1 % num2
  p totalmod
end

def square_root(num)
  totalsqrt = Integer.sqrt(num)
  p totalsqrt
end
