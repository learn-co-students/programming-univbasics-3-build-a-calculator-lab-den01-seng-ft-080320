def addition(num1, num2)
  add = num1 + num2
  add
end

def subtraction(num1, num2)
  minus = num1 - num2
  minus
end

def division(num1, num2)
  div = num1 / num2
  div
end

def multiplication(num1, num2)
  times = num1 * num2
  times
end

def modulo(num1, num2)
  remainder = num1 % num2
  remainder
end

def square_root(num)
  root = Math.sqrt(num)
  root
end